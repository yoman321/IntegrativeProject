package integrativeproject;

/**
 *
 * @author liamd
 */
public class FallingObject {
    private double x, y;
    
    public FallingObject(){
    }
    
    public void setX(double x){
        this.x = x;
    }
    public void setY(double y){
        this.y = y;
    }
    public double getX(){
        return x;
    }
    public double getY(){
        return y;
    }
    
}
